
package com.doublechaintech.promoengine;


public class PromoengineRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public PromoengineRuntimeException() {
        super();
    }


    public PromoengineRuntimeException(String message) {
        super(message);
    }


    public PromoengineRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public PromoengineRuntimeException(Throwable cause) {
        super(cause);
    }


   
}












